#include <stdio.h>

int main (){
	
	int inteira = 10;       //Usa o comando %i
	float decimal = 10.5;   //Usa o comando %f para ajustar as casa decimais colocar %0.2f
	char caractere = 'a';   // Usa o comando %c
	
	printf ("%i\n", inteira);
	printf("%0.2f\n", decimal);
	printf("%c\n", caractere);	
	
	
	return 0;
}
